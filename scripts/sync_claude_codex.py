#!/usr/bin/env python3
"""Sync safe Claude Code config surfaces into Codex surfaces.

This intentionally syncs only surfaces that have a clear Codex equivalent:

- repo `.claude/skills/*/(skill.md|SKILL.md)` -> `.agents/skills/*/SKILL.md`
- repo `.mcp.json` plus project MCP state in `~/.claude.json` -> `.codex/config.toml`
- repo code-review-graph hook intent -> `.codex/hooks.json`
- optional user skills: `~/.claude/skills/*/SKILL.md` -> `~/.agents/skills/*/SKILL.md`

It does not copy Claude plugin caches or arbitrary global hooks. Those need
Codex-native plugin/marketplace wiring or hook adapters.
"""

from __future__ import annotations

import argparse
import json
import os
import re
from pathlib import Path
from typing import Any


SENSITIVE_ENV = re.compile(r"(TOKEN|SECRET|PASSWORD|PASS|KEY|AUTH|CREDENTIAL)", re.I)

GRAPH_APPROVED_TOOLS = [
    "get_minimal_context_tool",
    "list_graph_stats_tool",
    "get_architecture_overview_tool",
    "semantic_search_nodes_tool",
    "query_graph_tool",
    "list_communities_tool",
    "get_community_tool",
    "list_flows_tool",
    "get_flow_tool",
    "detect_changes_tool",
    "get_review_context_tool",
    "get_impact_radius_tool",
    "get_affected_flows_tool",
    "find_large_functions_tool",
]


def load_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def write_if_changed(path: Path, content: str) -> bool:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and path.read_text(encoding="utf-8") == content:
        return False
    path.write_text(content, encoding="utf-8")
    return True


def toml_string(value: Any) -> str:
    return json.dumps(str(value))


def toml_array(values: list[Any]) -> str:
    return "[" + ", ".join(toml_string(v) for v in values) + "]"


def toml_table(parts: list[str]) -> str:
    quoted = [json.dumps(part) if not re.match(r"^[A-Za-z0-9_-]+$", part) else part for part in parts]
    return "[" + ".".join(quoted) + "]"


def normalize_skill_frontmatter(text: str, name: str) -> str:
    if not text.startswith("---\n"):
        return f"---\nname: {name}\ndescription: Ported from Claude Code.\n---\n\n{text}"

    end = text.find("\n---", 4)
    if end == -1:
        return text

    frontmatter = text[4:end].splitlines()
    body = text[end + len("\n---") :]
    saw_name = False
    out: list[str] = []

    for line in frontmatter:
        if line.startswith("name:"):
            out.append(f"name: {name}")
            saw_name = True
        elif line.startswith("trigger:"):
            # Claude slash-command trigger metadata is not part of Codex skill frontmatter.
            continue
        else:
            out.append(line)

    if not saw_name:
        out.insert(0, f"name: {name}")

    return "---\n" + "\n".join(out).strip() + "\n---" + body


def sync_skill_tree(source_root: Path, dest_root: Path) -> list[Path]:
    written: list[Path] = []
    if not source_root.exists():
        return written

    for skill_dir in sorted(p for p in source_root.iterdir() if p.is_dir()):
        source = skill_dir / "SKILL.md"
        if not source.exists():
            source = skill_dir / "skill.md"
        if not source.exists():
            continue

        name = skill_dir.name
        text = normalize_skill_frontmatter(source.read_text(encoding="utf-8"), name)
        dest = dest_root / name / "SKILL.md"
        if write_if_changed(dest, text):
            written.append(dest)

    return written


def project_claude_mcp_servers(repo_root: Path) -> dict[str, Any]:
    merged: dict[str, Any] = {}

    project_mcp = load_json(repo_root / ".mcp.json").get("mcpServers", {})
    if isinstance(project_mcp, dict):
        merged.update(project_mcp)

    claude_state = load_json(Path.home() / ".claude.json")
    project_state = claude_state.get("projects", {}).get(str(repo_root), {})
    project_servers = project_state.get("mcpServers", {})
    if isinstance(project_servers, dict):
        merged.update(project_servers)

    return merged


def render_mcp_config(repo_root: Path, servers: dict[str, Any]) -> str:
    lines = [
        "# Generated by scripts/sync_claude_codex.py.",
        "# Repo-specific MCP belongs here so global Codex config does not pin cwd to another project.",
        "",
    ]

    for name, server in sorted(servers.items()):
        if not isinstance(server, dict):
            continue

        lines.append(toml_table(["mcp_servers", name]))

        command = server.get("command")
        url = server.get("url")
        if command:
            lines.append(f"command = {toml_string(command)}")
        if url:
            lines.append(f"url = {toml_string(url)}")

        args = server.get("args")
        if isinstance(args, list):
            lines.append(f"args = {toml_array(args)}")

        cwd = server.get("cwd") or str(repo_root)
        if command:
            lines.append(f"cwd = {toml_string(cwd)}")

        transport = server.get("type")
        if transport:
            lines.append(f"type = {toml_string(transport)}")

        lines.append('default_tools_approval_mode = "prompt"')

        env = server.get("env")
        sensitive_env_names: list[str] = []
        plain_env: dict[str, str] = {}
        if isinstance(env, dict):
            for key, value in sorted(env.items()):
                if SENSITIVE_ENV.search(key):
                    sensitive_env_names.append(key)
                else:
                    plain_env[key] = str(value)

        if sensitive_env_names:
            lines.append(f"env_vars = {toml_array(sensitive_env_names)}")

        if plain_env:
            lines.append("")
            lines.append(toml_table(["mcp_servers", name, "env"]))
            for key, value in plain_env.items():
                lines.append(f"{key} = {toml_string(value)}")

        if name == "code-review-graph":
            lines.append("")
            for tool in GRAPH_APPROVED_TOOLS:
                lines.append(toml_table(["mcp_servers", name, "tools", tool]))
                lines.append('approval_mode = "approve"')
                lines.append("")
        else:
            lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_hooks(repo_root: Path) -> str:
    hooks = {
        "hooks": {
            "PostToolUse": [
                {
                    "matcher": "Edit|Write|Bash",
                    "hooks": [
                        {
                            "type": "command",
                            "command": (
                                'code-review-graph update --skip-flows --repo '
                                f'{json.dumps(str(repo_root))} >/dev/null 2>&1 || true'
                            ),
                            "timeout": 30,
                            "statusMessage": "Updating code-review-graph",
                        }
                    ],
                }
            ],
            "SessionStart": [
                {
                    "matcher": "startup|resume",
                    "hooks": [
                        {
                            "type": "command",
                            "command": (
                                'code-review-graph status --repo '
                                f'{json.dumps(str(repo_root))} || '
                                "echo 'code-review-graph status unavailable'"
                            ),
                            "timeout": 10,
                            "statusMessage": "Checking code-review-graph status",
                        }
                    ],
                }
            ],
        }
    }
    return json.dumps(hooks, indent=2) + "\n"


def sync_project(repo_root: Path) -> list[Path]:
    written: list[Path] = []

    written.extend(sync_skill_tree(repo_root / ".claude" / "skills", repo_root / ".agents" / "skills"))

    servers = project_claude_mcp_servers(repo_root)
    if servers:
        config = render_mcp_config(repo_root, servers)
        path = repo_root / ".codex" / "config.toml"
        if write_if_changed(path, config):
            written.append(path)

    if (repo_root / ".claude" / "settings.json").exists():
        path = repo_root / ".codex" / "hooks.json"
        if write_if_changed(path, render_hooks(repo_root)):
            written.append(path)

    return written


def sync_global_skills() -> list[Path]:
    return sync_skill_tree(Path.home() / ".claude" / "skills", Path.home() / ".agents" / "skills")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--repo-root", default=os.getcwd(), help="Repository root to sync")
    parser.add_argument(
        "--global-skills",
        action="store_true",
        help="Also sync ~/.claude/skills into ~/.agents/skills",
    )
    parser.add_argument(
        "--global-skills-only",
        action="store_true",
        help="Only sync ~/.claude/skills into ~/.agents/skills; do not touch repo files",
    )
    args = parser.parse_args()

    repo_root = Path(args.repo_root).resolve()
    written: list[Path] = []

    if not args.global_skills_only:
        written.extend(sync_project(repo_root))

    if args.global_skills or args.global_skills_only:
        written.extend(sync_global_skills())

    if written:
        print("Updated:")
        for path in written:
            print(f"- {path}")
    else:
        print("No changes.")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
